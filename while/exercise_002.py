#Other assignment operators
#  =	Assign
# +=	Add then assign
# -=	Subtract then assign
# *=	Multiply then assign
# /=	Divide then assign
# %=	Get the modulus then assign
# **=	Perform exponent then assign
# //=	Perform floor division then assign

count = 0
while count != 5:
    count += 1
    print(count)
print()

count = 0
while count <= 20:
    count += 3
    print(count)
print()  

count = 20
while count >= 0:
    count -= 3
    print(count)
print()
